# معرفی حلقه (while)
# برنامه ایی بنویسید که چندین عدد  را از ورودی گرفته مجموع آنها را نمایش دهد
# برنامه تازمانی از کاربر عدد بگیرد که کاربر اعداد کوچکتر از 100 وارد کند
# اگر کاربر عدد منفی وارد کرد برنامه مجمع اعداد گرفته شده قبلی را نمایش دهد
s=0
i = int(input())
while i <= 100:
  s += i
  i = int(input())
  if i<0:
     break 
print(s) 
