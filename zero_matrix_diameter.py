#برنامه ايي بنویسید که تشخیص دهد عناصر روی قطر اصلی صفر هستند یا خیر
f = [[0,21,-3,8],[21,0,-17,-6],[-3,-17,0,2],[8,-6,2,0]]

def ghotre_sefr(a):
    for i in range(len(a)):
        if a[i][i] != 0:
            print("عناصر روی قطر صفر نيستند")  
            return  
    print("عناصر روی قطر صفر هستند")    
    
ghotre_sefr(f)
