# معرفی حلقه (for)

for i in range(100): # این حلقه از 0 تا 99 تکرار می شود ولی
  pass               # هیچ کاری نمی کند چون در دستورات حلقه دستور (pass) نوشته ايم


# برنامه ایی بنویسید که یک عدد را گرفته مجموع اعدد زوج قبل از آن عدد را نمایش دهد
n = int(input())
s=0
for i in range(0,n,2):
  s += i
print(s) 
